
let a = 10;

console.log('como numero');
if (a == 10) {
    console.log('Es igual a '+a);
} else {
    console.log('No es igual a 10');
}

console.log('como string');
if (a == '10') {
    console.log('Es igual a '+a);
} else {
    console.log('No es igual a 10');
}

console.log('con compracion de tipo y contenido con ===');
if (a === '10') {
    console.log('Es igual a '+a+'en tipo y contenido');
} else {
    console.log('No es igual a 10 en tipo y contenido');
}

